Route::get('/storage/images/portrait/{filename}', function ($filename)
{
    $path = storage_path('/app/images/portrait/' . $filename);


    if (!File::exists($path)) {
        abort(404);
    }

    $file = File::get($path);
    $type = File::mimeType($path);

    $response = Response::make($file, 200);
    $response->header("Content-Type", $type);

    return $response;
});
